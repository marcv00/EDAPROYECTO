    public void CargarTabla()
        {
            int filas = this.jTable1.getRowCount();
            for (int i =0;i<filas;i++)
            {
                modelo.removeRow(0);
            }
            String nombre = CrearDependencia.probar.getNombre();
            String[] dato = new String[1];
            dato[0]=nombre;
            modelo.addRow(dato);
                 
        }

