package model;

/**
 *
 * @author l33212
 */
public class Interesado extends Persona {

    private String DNI;
    
    public Interesado(String DNI, String nombre, String correo) {
        super(nombre, correo);
        this.DNI = DNI;
    }

    public String getDNI() {
        return DNI;
    }

    public void setDNI(String DNI) {
        this.DNI = DNI;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }
    
    
    
}
